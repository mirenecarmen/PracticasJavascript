/*definí la función maximo, que funcione como Math.max (¡no vale usarla!) y retorne el máximo
 entre dos números. 
Por ejemplo, el máximo entre 4 y 5 es 5, y el máximo entre 10 y 4, es 10. */

function maximo(maximo,minimo) {
    if (maximo >= minimo) {
      return maximo;
    } else {
      return minimo;
    }
 }